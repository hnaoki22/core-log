// (token, date, slot) から決定論的に1案を選ぶ（既存 getPlaceholderExample と同じ djb2 seed 方式）。
// 同じ人・同じ日・同じ問いなら常に同じ例、日付が変わると別の例に切り替わる。
export function getPlaceholderFromPool(
  slot: PlaceholderSlot,
  token: string,
  date: string
): string {
  const pool = PLACEHOLDER_POOL[slot];
  if (!pool || pool.length === 0) return "";
  const seed = `${token}-${date}-${slot}`;
  let h = 5381;
  for (let i = 0; i < seed.length; i++) {
    h = ((h << 5) + h + seed.charCodeAt(i)) >>> 0;
  }
  return pool[h % pool.length];
}
